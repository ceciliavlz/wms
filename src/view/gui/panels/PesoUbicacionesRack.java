    private void pintarPesos(){
        List<String> pesosResp = NaveController.getInstance().calcularPesosRack(idRack);
        
        Collections.sort(pesosResp);
        String[] pesos = pesosResp.toArray(String[]::new);
        
        int index = 0;
        
        for (int i = 1; i < 4; i++){
            for (int j = 1; j < 4; j++){
                String nombre = "pesoF" + i + "C" + j;
                String peso = pesos[index].split(" ")[1];
                String codigoUbicacion = pesos[index].split(" ")[0].split(":")[0];

                JLabel label = buscarLabelPorNombreEnPanel(this, nombre);
                
                if (label != null) {
                    boolean llena = NaveController.getInstance().ubicacionLlena(codigoUbicacion);
                    Component padre = label.getParent();
                    
                    if (!llena){
                        label.setText(peso);
                        padre.setBackground(new Color(232,232,232));
                    } else {
                        padre.setBackground(new Color(229,155,129));
                        label.setText("LLENO");
                    }
                    
                    index++;
                } else {
                    System.out.println("No existe ese JLabel.");
                }
            }
        }
    }
    
    public JLabel buscarLabelPorNombreEnPanel(Container container, String nombreBuscado) {
        for (Component comp : container.getComponents()) {

            if (comp instanceof JLabel && nombreBuscado.equals(comp.getName())) {
                return (JLabel) comp;
            }

            if (comp instanceof Container) {
                JLabel resultado = buscarLabelPorNombreEnPanel((Container) comp, nombreBuscado);
                if (resultado != null) return resultado;
            }
        }
        
        return null;
    }
    
