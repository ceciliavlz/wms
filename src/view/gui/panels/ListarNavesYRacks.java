    private void mostrarListaNaves(){
        if (naves.isEmpty()){
            noHayNaves.setVisible(true);

        } else if (naves.size() > 4){
            navesComboBox.removeAllItems();
            comboBoxNaves();

        } else {
            botonesNave();
        }
    }
    
    private void comboBoxNaves(){
        botonSeleccionar.setVisible(true);
        navesComboBox.setVisible(true);
        
        for (String nave : naves) {
            navesComboBox.addItem(nave);
        }
    }
    
    private void botonesNave(){
        navesPanel.setLayout(new BoxLayout(navesPanel, BoxLayout.X_AXIS));
        navesPanel.setBorder(BorderFactory.createEmptyBorder(10, 10, 10, 10));
        
        ImageIcon icono = new ImageIcon("Resources/img/nave.png");
        
        for (String nave : naves) {
            JLabel label = new JLabel(nave, icono, JLabel.LEFT);
            label.setFont(new Font("Roboto", Font.BOLD, 18));
            label.setForeground(Color.WHITE);
            label.setBorder(BorderFactory.createEmptyBorder(15, 20, 15, 0));
            label.setCursor(Cursor.getPredefinedCursor(Cursor.HAND_CURSOR));
            
            label.addMouseListener(
                new java.awt.event.MouseAdapter() {
                    @Override
                    public void mouseClicked(java.awt.event.MouseEvent evt) {
                        mostrarRacks(nave); 
                    }
                }
            );
            
            navesPanel.add(label);
        }
        
        navesPanel.revalidate();
        navesPanel.repaint();
    }
    
    private void mostrarRacks(String nombreSeleccionado) {
        modelo.clear();
        
        int id = Integer.parseInt(nombreSeleccionado.split(" ")[2]);
        List<String> racksRecibidos = NaveController.getInstance().listarRacks(id);
        
        if (racksRecibidos.isEmpty()){
            modelo.addElement("No hay racks asociados.");

        } else {
            int i = 1;
            
            for (String rack : racksRecibidos){
                int idRack = Integer.parseInt(rack.split(":")[1].split(",")[0].trim());
                String peso = NaveController.getInstance().pesoTotalRack(idRack);
                modelo.addElement(i + ". " + rack + ". Peso actual " + peso);
                System.out.println(rack);
                i++;
            }
        }
        
        racks.setModel(modelo);

        // Hacer visible la lista si estaba oculta
        racksTitulo.setVisible(true);
        racks.revalidate();
        racks.repaint();
    }
   
